DROP TABLE IF EXISTS EMPLOYEE_MASTER;
 
CREATE TABLE EMPLOYEE_MASTER (
  emp_id INT AUTO_INCREMENT  PRIMARY KEY,
  emp_first_name VARCHAR(250) NOT NULL,
  emp_last_name VARCHAR(250) NOT NULL,
  emp_addr VARCHAR(250) DEFAULT NULL
);

DROP TABLE IF EXISTS EMP_PUNCH_LOG;
CREATE TABLE EMP_PUNCH_LOG (
  emp_punch_log_id INT AUTO_INCREMENT  PRIMARY KEY,
  emp_punch_status VARCHAR(10) NOT NULL,
  emp_punch_timestamp TIMESTAMP NOT NULL,
  emp_id INT NOT NULL
);
ALTER TABLE EMP_PUNCH_LOG
ADD FOREIGN KEY (emp_id) 
REFERENCES EMPLOYEE_MASTER(emp_id);